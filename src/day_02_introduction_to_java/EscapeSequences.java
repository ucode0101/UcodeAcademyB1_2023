package day_02_introduction_to_java;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("My name\tJava");
        System.out.println("Hello\nJame\nand\nMike");

        System.out.println("I love \"java\"");

        System.out.println("Java\\JDK");
        System.out.println("Java\\Coding");

        System.out.println("I love \'Java\'");

        System.out.println("Someone's phone");


    }
}
