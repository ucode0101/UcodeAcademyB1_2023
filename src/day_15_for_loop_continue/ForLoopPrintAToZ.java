package day_15_for_loop_continue;

public class ForLoopPrintAToZ {
    public static void main(String[] args) {

        for (char c ='a'; c<='z'; c++){
            System.out.print(c+" ");
        }
        System.out.println();

        for (char ch ='A'; ch <='Z'; ch++){
            System.out.print(ch+" ");
        }
    }
}
