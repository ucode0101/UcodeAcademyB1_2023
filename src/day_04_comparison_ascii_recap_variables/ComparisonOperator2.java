package day_04_comparison_ascii_recap_variables;

public class ComparisonOperator2 {

    public static void main(String[] args) {

        System.out.println(5 == 1);
        // = assignment operator -> we use it to assign value to variable
        // == equal operator -> we use it to compare two values. return boolean

        boolean isEqual = 5 == 15;
        System.out.println(isEqual);

        int num;
        num = 10;
        System.out.println(num);

    }
}
